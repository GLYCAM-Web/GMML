    std::vector<int> solvent_pointers = std::vector<int>();
    std::vector<int> atoms_per_molecule = std::vector<int>();
    std::vector<double> box_dimensions = std::vector<double>();
    std::vector<std::string> radius_sets = std::vector<std::string>();
